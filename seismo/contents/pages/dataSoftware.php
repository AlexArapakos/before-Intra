  <h3><a name="software"></a>Software</h3>
  <ul>
	<li>
	  <b>Interface</b><br/><br/>
	  <ul>
		<li><a href="http://php.net/" target="_blank">PHP</a> has been used for the main application which use
			<a href="http://www.apache.org/" target="_blank">Apache</a> as a Web server.
		</li><br/>
		<li><a href="http://maps.google.com/" target="_blank">Google Maps</a> provide us the maps and make this application WebGIS application.</li><br/>
		<li>It has been used also other technologies as
			<a href="http://javascript.internet.com/" target="_blank">Javascript</a>,&nbsp;
			<a href="http://www.dynamicajax.com/" target="_blank">Ajax</a>,&nbsp;
			<a href="http://www.xml.com/" target="_blank">XML</a>, HTML and Cascade Style Sheets.
		</li><br/>
		<li>For the Datawarehouse interface it has been used <a href="http://java.sun.com/products/jsp/" target="_blank">JSP</a>
			which use <a href="http://jakarta.apache.org/tomcat/" target="_blank">Apache Jakarta Tomcat</a> as a Web server.
		</li><br/>
	  </ul>  
	</li><br/>
	<li>
	  <b>Database & Datawarehouse</b><br/><br/>
	  <ul>
		<li>Oracle 10g with Spatial has been used for all database, spatial, data mining and document operations.
			<a href="http://www.oracle.com/technology/products/bi/9idmining.html" target="_blank">Oracle Data Mining</a>
			,&nbsp;<a href="http://www.oracle.com/technology/products/spatial/index.html" target="_blank">Oracle Spatial</a>
			&nbsp;and <a href="http://www.oracle.com/technology/products/intermedia/index.html" target="_blank">Oracle
			interMedia</a> with their SQL and Java interfaces have been the most useful features for the application.
		</li><br/>
		<li><a href="http://www.oracle.com/technology/products/database/workspace_manager/index.html" target="_blank">Oracle Analytic Workspace Manager</a>
			has been used for the designation of Datawarehouse. Also 
			<a href="http://www.oracle.com/technology/products/jdev/index.html" target="_blank">Oracle JDeveloper</a>
			with <a href="http://www.oracle.com/technology/products/bib/index.html" target="_blank">Oracle BI Beans</a>
			has been used for creation of the JSP code.
		</li><br/>
	  </ul>
	</li>
  </ul>
  <h3><a name="data"></a>Data</h3>
  <ul>
	<li>
	  <b>Seismic data (hypocenters and magnitudes)</b><br/><br/>
	  <ul>
		<li><a href="http://www.gein.noa.gr/services/Noa_cat/CAT2003.TXT" target="_blank">GI-NOA monthly list</a>
			: Web page which contains seismic data for the events of the last month in the hellenic area.
		</li><br/>
		<li><a href="http://neic.usgs.gov/neis/epic/epic_global.html" target="_blank">Earthquake
			Hazards Program: Global Earthquake Search</a>: Web form that allows selection of the necessary
			parameters to perform a GLOBAL search for earthquakes.
		</li><br/>
		<li>GI-NOA relational database: This was given to us by researcher
			<a href="http://www.gein.noa.gr/Kalogeras/cveng.html" target="_blank">Dr. I.S. Kalogeras</a>
			and contains (among others) seismic data with magnitude greater than 5 from 1901 until 2004.
        </li><br/>
		<li>Catalogue of Comninakis and Papazachos: This catalog contains data from 1010 until 1963.</li><br/>					
	  </ul>
	</li><br/>
	<li>
	  <b>Macroseismic data (intensity data)</b><br/><br/>
	  <ul>
		<li>GI-NOA relational database: This database contains macroseismic data for some major
			earthquakes in the hellenic area.
		</li><br/>
	  </ul>
	</li><br/>
	<li>
	  <b>Administrative data (site names, population, coordinates e.t.c.)</b><br/><br/>
	  <ul>
		<li>GI-NOA relational database: This database contains administrative data for more than 6000
			sites in Greece.
		</li><br/>
	  </ul>
	</li><br/>
	<li>
	  <b>Multimedia data (images, webpages, documents e.t.c.)</b><br/><br/>
	  <ul>
		<li><a href="http://www.gein.noa.gr/Kalogeras/cveng.html" target="_blank">Dr. I.S. Kalogeras</a> 
			webpage from GI-NOA site: Contains pictures and	webpages from important earthquakes in Greece.
		</li><br/>
	  </ul>
	</li><br/>
	<li>
	  <b>Flinn Engdahl geographic and seismic regions</b><br/><br/>
	  <ul>
		<li><a href="http://neic.usgs.gov/neis/epic/fer.html" target="_blank">Earthquake
			Hazards Program: Flinn-Engdahl Regions</a><br>
		</li><br/>
	  </ul>
	</li><br/>
