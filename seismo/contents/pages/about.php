<h3><a name="seismo">Seismo-Surfer Project Information</a></h3><br/>
Seismo-Surfer is a WebGIS application for integrating, visualizing and analyzing seismic data. Its main features are:<br/>

<ul>
	<li>GIS functionality: Zooming, navigating, saving, maximizing and printing the map. Different types of maps.<br/>
		Dialogs for displaying feature attributes. Measure distance.
	</li>
	<li>Queries for monitoring and analyzing earthquake activity: Range, Distance-Based, Nearest-Neighbour, Top-N e.t.c.</li>
	<li>Collection and integration of seismic data: Seismic data from web sources, multimedia data related with earthquakes,<br/>
	    macroseismic data from the Athens Geodynamic Institute and administrative data are all integrated in a single database and<br/>
		are made available through the tool.
	</li>
	<li>A data warehouse module for finding paterns.</li>
	<li>A data mining module for knowledge extraction coming soon.</li>
</ul>

Seismo-Surfer is being developed at the 
<a href="http://www.cs.unipi.gr" target="_blank">Department of Informatics</a> in the 
<a href="http://www.unipi.gr/eng_site/default.php" target="_blank">University of Piraeus</a> with the cooperation of the 
<a href="http://www.gein.noa.gr/index-en.htm" target="_blank">Geodynamic Institute</a> of the National Observatory of Athens.<br/>
People involved are <a href="http://www.unipi.gr/faculty/ytheod/" target="_blank">Asst. Prof. Yannis Theodoridis</a>, PhD student 
<a href="http://isl.cs.unipi.gr/db/people.html#makis" target="_blank">G. Marketos</a> and researcher 
<a href="http://www.gein.noa.gr/Kalogeras/cveng.html" target="_blank">Dr. I.S. Kalogeras</a>.<br/><br/>

