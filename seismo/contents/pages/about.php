<h3><a name="project">Project</a></h3><br/>
Seismo-Surfer is being developed and deployed using PHP and JSP for the warehouse, open source software 
(Apache, GoogleMaps) and object-relational DBMS with support for spatial data types and operations (Oracle 10g with Spatial).<br/>
Source code and documentation will be available soon for download.<br/><br/>

